<p>In the Summer of 2006 I visited Aups, a small town in the very south of France, where I bought a copy of Gigamic's wooden board game Quoridor. I liked it so much, I decided to turn it into a computer game. I also wrote several computer opponents&mdash;brains&mdash;for it, and set the code up in such a way that you can contribute your own brains. Are you up for a challenge? Download the SDK, write your own brain and let me know!</p>

<p>Latest release: r@rev.</p>

<p>You will need the Java Runtime Environment 5.0. For Mac OS 10.4, this is a Software Update. Users of other platforms might want to <a href="http://java.sun.com/javase/downloads/">download from Sun</a>.</p>

